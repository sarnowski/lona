// =========================================================================
// Tail Position
// =========================================================================

#[test]
fn compile_case_in_fn_tail_position() {
    // When case is in tail position, results should use TailCall for function calls
    let chunk = compile_source("(fn [] (case 1 1 (recur)))");
    let _code = chunk.code();

    // Find TailCall in the generated code (for the recur call in the case result)
    // The function body is in the constant pool
    if let Some(Constant::Function { bodies, .. }) = chunk.get_constant(0) {
        let body = bodies.first().expect("function should have a body");
        let body_code = body.chunk.code();

        // The recur in the result should be compiled as TailCall
        let has_tail_call = body_code
            .iter()
            .any(|&instr| decode_op(instr) == Some(Opcode::TailCall));
        assert!(
            has_tail_call,
            "case result in tail position should emit TailCall for recursive calls"
        );
    } else {
        panic!("expected Function constant");
    }
}

