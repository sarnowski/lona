#[test]
fn compile_case_with_else() {
    let chunk = compile_source("(case 1 2 :two :else :default)");
    let code = chunk.code();

    // With :else, there should be NO CaseFail opcode
    let has_case_fail = code
        .iter()
        .any(|&instr| decode_op(instr) == Some(Opcode::CaseFail));
    assert!(
        !has_case_fail,
        "should not have CaseFail when :else is present"
    );
}

#[test]
fn compile_case_else_must_be_last() {
    let interner = symbol::Interner::new();
    let result = compile("(case 1 :else :default 2 :two)", TEST_SOURCE_ID, &interner);
    assert!(result.is_err());

    if let Err(CompileError::Compile(Error {
        kind: ErrorKind::InvalidSpecialForm { form, message },
        ..
    })) = result
    {
        assert_eq!(form, "case");
        assert!(message.contains("last clause"));
    } else {
        panic!("expected InvalidSpecialForm error");
    }
}

