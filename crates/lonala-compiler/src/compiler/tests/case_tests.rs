//! Tests for the `case` special form compiler.
//!
//! Tests cover:
//! - Basic pattern matching (integers, keywords, strings, nil, booleans)
//! - Default clause with `:else`
//! - Invalid patterns (symbols, lists, vectors, maps, floats)
//! - Duplicate pattern detection
//! - Tail position propagation

use lona_core::chunk::Constant;
