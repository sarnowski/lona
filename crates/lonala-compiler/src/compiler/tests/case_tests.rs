// =========================================================================
// Basic Case Compilation
// =========================================================================

#[test]
fn compile_case_integer_patterns() {
    let chunk = compile_source("(case 1 1 :one 2 :two 3 :three)");
    let code = chunk.code();

    // Should have:
    // LoadK R0, K0 (1 - test value)
    // LoadK R1, K1 (1 - pattern)
    // Eq R2, R0, R1
    // JumpIfNot R2, ...
    // LoadK R1, K2 (:one)
    // Move R0, R1
    // Jump ...
    // ... (more patterns)
    // CaseFail R0 (no :else)

    // Verify test value is loaded
    let first_instr = *code.first().expect("code should not be empty");
    assert_eq!(decode_op(first_instr), Some(Opcode::LoadK));

    // Find CaseFail at the end (before Return)
    let last_before_return = code.get(code.len().saturating_sub(2));
    assert!(
        last_before_return.is_some(),
        "should have instruction before return"
    );
    let case_fail_instr = *last_before_return.expect("last_before_return is Some");
    assert_eq!(decode_op(case_fail_instr), Some(Opcode::CaseFail));
}

#[test]
fn compile_case_keyword_patterns() {
    let (chunk, interner) = compile_with_interner("(case :a :a 1 :b 2)");
    let code = chunk.code();

    // Verify we have bytecode (detailed instruction checking is implementation-specific)
    assert!(!code.is_empty(), "should have generated bytecode");

    // Verify the constants include our keywords (stored as Constant::Keyword)
    let keywords: alloc::vec::Vec<_> = chunk
        .constants()
        .iter()
        .filter_map(|c| {
            if let Constant::Keyword(kw_id) = c {
                Some(interner.resolve(*kw_id))
            } else {
                None
            }
        })
        .collect();
    assert!(
        keywords.iter().any(|k| k == "a"),
        "should have keyword constant for :a"
    );
    assert!(
        keywords.iter().any(|k| k == "b"),
        "should have keyword constant for :b"
    );
}

#[test]
fn compile_case_string_patterns() {
    let chunk = compile_source(r#"(case "hello" "hello" 1 "world" 2)"#);
    let code = chunk.code();

    // Verify first instruction loads the test string
    let first_instr = *code.first().expect("code should not be empty");
    assert_eq!(decode_op(first_instr), Some(Opcode::LoadK));
    let const_idx = decode_bx(first_instr);

    // Constant should be the "hello" string
    assert_eq!(
        chunk.get_constant(const_idx),
        Some(&Constant::String(alloc::string::String::from("hello")))
    );
}

#[test]
fn compile_case_nil_pattern() {
    let chunk = compile_source("(case nil nil 1)");
    let code = chunk.code();

    // Verify first instruction loads nil (uses LoadNil opcode, not LoadK)
    let first_instr = *code.first().expect("code should not be empty");
    assert_eq!(decode_op(first_instr), Some(Opcode::LoadNil));

    // The pattern nil should also be present in constants for comparison
    let has_nil = chunk.constants().iter().any(|c| matches!(c, Constant::Nil));
    assert!(has_nil, "should have nil constant for pattern comparison");
}

